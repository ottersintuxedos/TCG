CaterpieCard: ; 30e28 (c:4e28)
	db TYPE_PKMN_GRASS ; type
	gfx CaterpieCardGfx ; gfx
	tx CaterpieName ; name
	db CIRCLE ; rarity
	db DISCOVERY | NONE ; sets
	db CATERPIE
	db 50 ; hp
	db BASIC ; stage
	dw None ; pre-evo name

	; attack 1
	energy COLORLESS, 1 ; energies
	tx SpinAttackName ; name
	tx SpinAttackDescription ; description
	dw NONE ; description (cont)
	db 20 ; damage
	db DAMAGE_NORMAL ; category
	dw SpinAttackEffectCommands ; effect commands
	db NONE ; flags 1
	db LOW_RECOIL ; flags 2
	db NONE ; flags 3
	db 1
	db ATK_ANIM_SPIT_POISON; animation

	; attack 2
	energy 0 ; energies
	dw NONE ; name
	dw NONE ; description
	dw NONE ; description (cont)
	db 0 ; damage
	db DAMAGE_NORMAL ; category
	dw NONE ; effect commands
	db NONE ; flags 1
	db NONE ; flags 2
	db NONE ; flags 3
	db 0
	db ATK_ANIM_NONE ; animation

	db 2 ; retreat cost
	db WR_FIRE ; weakness
	db NONE ; resistance
	tx Worm ; category
	db 10 ; Pokedex number
	db 0
	db 16 ; level
	db 1, 0 ; length
	dw 6 * 10 ; weight
	tx CaterpieDescription ; description
